    private void lblusuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblusuariosMouseClicked
       try {
        boolean mostrar=true;
        String Nombre=frmttodousuario.getTitle();
        for (int a=0;a<escritorio.getComponentCount();a++){     // verificar si es instancia de algun componente que ya este en el jdesktoppane
        if( frmttodousuario.getClass().isInstance( escritorio.getComponent(a) )){
        // JOptionPane.showMessageDialog(rootPane, "La ventana "+Nombre+" que interta abrir ya est치 abierta, cierre la ventana actual e intente nuevamente");
        System.out.println("esta instancia, no se debe mostrar");
        frmttodousuario.toFront();
        escritorio.moveToFront(frmttodousuario);
        mostrar=false;
     }else{
        System.out.println("no lo es, puede mostrarse");
    }
}
        if(mostrar){
        Dimension desktopSize = escritorio.getSize();
        frmttodousuario.setSize(escritorio.getSize());
        escritorio.add(frmttodousuario);
        ((javax.swing.plaf.basic.BasicInternalFrameUI)frmttodousuario.getUI()).setNorthPane(null);//Con esto quito el title
        }
         frmttodousuario.show();
        } catch (Exception e) {
          JOptionPane.showConfirmDialog(null,"Error: " + e);
        }
    }//GEN-LAST:event_lblusuariosMouseClicked

